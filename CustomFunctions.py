def greet(name1,name2,name3):
    return f'Hello, {name1}, {name2} and {name3}!'

print(greet('Pat','Splat','Grat'))

def add(x,y):
    return x + y

print(str(add(1,2)))

def add_names(name1,name2):
    return name1 + name2

print(add_names('Pat','Splat'))

def seconds_in_week(x):
    return x * 604800

print(str(seconds_in_week(5)))
